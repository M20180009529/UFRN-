function soma(a,b)
	{
		return a+b;
	}
function subtração(a,b)
	{
		return a-b;
	}
function multiplicação(a,b)
	{
		return a*b;
	}
function divisao(a,b)
	{
		return a/b;
	}
function resto(a,b)
	{
		return a%b;
	}
function PI()
	{
		return Math.PI;
	}
function potencia(a,b)
	{
		return Math.pow(a,b);
	}	
